package com.zlate87.fyberapp.feature.offers.model;

/**
 * Model class.
 */
public class OfferParameters {
	private String uid;
	private String apiKey;
	private String appid;
	private String pub0;
	private String format;
	private String osVersion;
	private String timestamp;
	private String googleAddId;
	private String googleAdIdLimitedTrackingEnabled;
	private String locale;
	private String ip;

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getApiKey() {
		return apiKey;
	}

	public void setApiKey(String apiKey) {
		this.apiKey = apiKey;
	}

	public String getAppid() {
		return appid;
	}

	public void setAppid(String appid) {
		this.appid = appid;
	}

	public String getPub0() {
		return pub0;
	}

	public void setPub0(String pub0) {
		this.pub0 = pub0;
	}

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public String getOsVersion() {
		return osVersion;
	}

	public void setOsVersion(String osVersion) {
		this.osVersion = osVersion;
	}

	public String getTimestamp() {
		return timestamp;
	}

	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}

	public String getGoogleAddId() {
		return googleAddId;
	}

	public void setGoogleAddId(String googleAddId) {
		this.googleAddId = googleAddId;
	}

	public String getGoogleAdIdLimitedTrackingEnabled() {
		return googleAdIdLimitedTrackingEnabled;
	}

	public void setGoogleAdIdLimitedTrackingEnabled(String googleAdIdLimitedTrackingEnabled) {
		this.googleAdIdLimitedTrackingEnabled = googleAdIdLimitedTrackingEnabled;
	}

	public String getLocale() {
		return locale;
	}

	public void setLocale(String locale) {
		this.locale = locale;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}
}
